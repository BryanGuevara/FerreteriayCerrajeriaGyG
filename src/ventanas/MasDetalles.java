    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Nombre = jTextField1.getText().trim();
        Connection cnz = Conexiones.conexion();
        try {
            cnz = Conexiones.conexion();
            PreparedStatement pstz = cnz.prepareStatement(
                    "insert into pronombres values(?,?,?)");

            pstz.setString(1, "0");
            pstz.setString(2, Nombre);
            pstz.setInt(3, ID);

            pstz.executeUpdate();
            cnz.close();
        } catch (Exception e) {
            System.out.println(e);
        }
        JOptionPane.showMessageDialog(null, "A単adido Exitosamente");

        try {
            cnz = Conexiones.conexion();
            PreparedStatement pst = cnz.prepareStatement(
                    "select ID, Nombre from pronombres where IdProducto = '" + ID + "'");
            ResultSet rs = pst.executeQuery();

            for (int i = 0; i < Table.getRowCount(); i++) {
                model.removeRow(i);
                i--;
            }

            while (rs.next()) {
                Object[] fila = new Object[2];

                for (int i = 0; i < 2; i++) {

                    fila[i] = rs.getObject(i + 1);

                }
                model.addRow(fila);
            }
        } catch (Exception e) {
            System.out.println("Error al llenar la tabla de pronombres: " + e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

